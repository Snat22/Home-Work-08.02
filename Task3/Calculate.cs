namespace Task3;

static class Calculate
{
    public static int Add(int x, int y)
    {
       return x + y;
    }
    public static int Subtract(int x, int y)
    {
        return x - y;
    }
    public static int Multiply(int x, int y)
    {
        return x * y;
    }
    public static int Devide(int x, int y) 
    {
        return x / y;
    }
}
